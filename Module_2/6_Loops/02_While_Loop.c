#include <stdio.h>

int main()
{
    // also called loop control variable

    int count = 0; // initialize a counter variable

    // loop while count is less than 5

    while (count < 5)
    {

        printf("Hello, world!\n"); // print the text

        count++; // increment the counter}
    }
    return 0;
}

